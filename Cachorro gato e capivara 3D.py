def gerar_curva_bezier_cubica(p0, p1, p2, p3, numero_de_pontos=15):
    t_valores = np.linspace(0, 1, numero_de_pontos)
    curva_pontos = []
    for t in t_valores:
        b0 = (1-t)**3
        b1 = 3 * t * (1-t)**2
        b2 = 3 * (t**2) * (1-t)
        b3 = t**3
        curva_pontos.append((b0*p0[0] + b1*p1[0] + b2*p2[0] + b3*p3[0], b0*p0[1] + b1*p1[1] + b2*p2[1] + b3*p3[1]))
    return curva_pontos

